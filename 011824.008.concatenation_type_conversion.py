'''
008. Concatenation & convertion
Concatenar es unir variables o strings (no numeros) ya que es necesario concertirlos en string para concatenar, ya que si no los suma y usamos el simbolo de "+"
'''

first_name = 'Ibrahim'
last_name = 'Guerra'

print(first_name + ' ' + last_name)

full_name = first_name + ' ' + last_name

print(full_name)

born_year = 1945

print(full_name + ' ' + str(born_year))

current_year = 2024

print(str(born_year) + ' ' + str(current_year))
